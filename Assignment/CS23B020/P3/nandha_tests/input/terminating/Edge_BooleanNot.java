class Edge_BooleanNot {
  public static void main(String[] args) {
    int one;
    int zero;
    boolean a;
    boolean b;
    boolean c;
    one = 1;
    zero = 0;
    a = true;
    b = !a;
    c = !b;
    if (c) {
      System.out.println(one);
    } else {
      System.out.println(zero);
    }
  }
}
